package com.smartcommunities.xdengue.dataModel;

public class ReportTimeInterval {
	private int Minutes;
	private int Hourse;
	private int Days;
	public int getMinutes() {
		return Minutes;
	}
	public void setMinutes(int minutes) {
		Minutes = minutes;
	}
	public int getHourse() {
		return Hourse;
	}
	public void setHourse(int hourse) {
		Hourse = hourse;
	}
	public int getDays() {
		return Days;
	}
	public void setDays(int days) {
		Days = days;
	}
}
